private class Anuncio{
	public double valor;
	public String carac;
	public String garac;
	public String fret;
	
	public void setVal(double valor){this.valor=valor;}
	public double getVal(){return valor;}

	public void setCarac(String carac) {
		this.carac = carac;
	}
	public String getCarac() {
		return carac;
	}
	public void setGarac(String garac) {
		this.garac = garac;
	}
	public String getGarac() {
		return garac;
	}
	public void setFret(String fret) {
		this.fret = fret;
	}
	public String getFret() {
		return fret;
	}
}
